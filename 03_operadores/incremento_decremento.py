year = 2021

# Incremento y decremento numérica de forma manual.

print("Incremento y decremento manual.\n")

year = year + 1
print(year)

year = year - 1
print(year)

print("-------------------\n")

print("Incremento y decremento automática.\n")

year += 1
print(year)

year -= 1
print(year)

"""
CRITERIO:

De forma manual: variable = variable + o - número

De forma automática: += número (Incremento) / -= número (Decremento)
"""

