"""
***** OPERADORES ARITMETICOS-MATEMÁTICOS *****

Suma = +
Resta = -
Multiplicación = *
División = /

Modulación o resto de una división = %

NOTA: El operador aritmetico de modulación es también utilizado muy a menudo por los programadores, para saber ->
-> sí un número es par o impar.
"""

numero1 = 35
numero2 = 88   # Operador de asignación (=)

resta = (numero2 - numero1)
suma = (numero2 + numero1)
multiplicacion = (numero2 * numero1)
division = (numero2 / numero1)
modulador = (numero2 % numero1)

print("***** RESULTADOS *****\n")
print(f"El resultado de la resta es: {resta}")
print(f"El resultado de la suma es: {suma}")
print(f"El resultado de la multiplicación es: {multiplicacion}")
print(f"El resultado de la división es: {division}")
print(f"El resultado de la modulación es: {modulador}")

